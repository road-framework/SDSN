public class GetServiceReturn {
    private String raasRef;
    private String guid;
    private String name;
    private String provider;
    private String publicationTime;
    private String version;
    private Capabilities capabilities;
    private String granularity;
    private String nature;
    private String releaseStage;
    private TechnicalInterfaces technicalInterfaces;

    public String getRaasRef() {
        return raasRef;
    }

    public void setRaasRef(String raasRef) {
        this.raasRef = raasRef;
    }

    public String getGuid() {
        return guid;
    }

    public void setGuid(String guid) {
        this.guid = guid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getProvider() {
        return provider;
    }

    public void setProvider(String provider) {
        this.provider = provider;
    }

    public String getPublicationTime() {
        return publicationTime;
    }

    public void setPublicationTime(String publicationTime) {
        this.publicationTime = publicationTime;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public Capabilities getCapabilities() {
        return capabilities;
    }

    public void setCapabilities(Capabilities capabilities) {
        this.capabilities = capabilities;
    }

    public String getGranularity() {
        return granularity;
    }

    public void setGranularity(String granularity) {
        this.granularity = granularity;
    }

    public String getNature() {
        return nature;
    }

    public void setNature(String nature) {
        this.nature = nature;
    }

    public String getReleaseStage() {
        return releaseStage;
    }

    public void setReleaseStage(String releaseStage) {
        this.releaseStage = releaseStage;
    }

    public TechnicalInterfaces getTechnicalInterfaces() {
        return technicalInterfaces;
    }

    public void setTechnicalInterfaces(TechnicalInterfaces technicalInterfaces) {
        this.technicalInterfaces = technicalInterfaces;
    }

}
